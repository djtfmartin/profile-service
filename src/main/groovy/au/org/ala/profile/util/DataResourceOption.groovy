package au.org.ala.profile.util

enum DataResourceOption {
    /**
     * All available collectory data resources
     */
    ALL,
    /**
     * No additional resources
     */
            NONE,
    /**
     * One or more Data Hubs from the collectory
     */
            HUBS,
    /**
     * One or more Data Resources from the collectory
     */
            RESOURCES
}